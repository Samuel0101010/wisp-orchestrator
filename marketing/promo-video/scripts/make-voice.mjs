function wavDurationSeconds(path) {
  const b = readFileSync(path);
  const sampleRate = b.readUInt32LE(24);
  const channels = b.readUInt16LE(22);
  const bits = b.readUInt16LE(34);
  const dataBytes = b.length - 44;
  return dataBytes / (sampleRate * channels * (bits / 8));
}

const seconds = [];
const frames = [];
LINES.forEach((text, i) => {
  const out = resolve(OUT, `vo-${i + 1}.wav`);
  execFileSync(PIPER, ['-m', MODEL, '-f', out, '--length_scale', '1.0', '--sentence_silence', '0.25'], {
    input: text,
    stdio: ['pipe', 'ignore', 'inherit'],
  });
  const dur = wavDurationSeconds(out);
  const fr = Math.ceil(dur * FPS);
  seconds.push(Number(dur.toFixed(2)));
  frames.push(fr);
  console.log(`vo-${i + 1}.wav  ${dur.toFixed(2)}s  ${fr}f  ${(statSync(out).size / 1024).toFixed(0)}KB  | ${text}`);
});

const totalVo = seconds.reduce((a, b) => a + b, 0);
writeFileSync(
  META,
  `// AUTO-GENERATED by scripts/make-voice.mjs — do not edit by hand.\n` +
    `// Voice-over clip lengths (vo-1..vo-${LINES.length}.wav) at ${FPS}fps. scenes.config.ts\n` +
    `// sizes every narrated scene from these so visuals stay locked to the audio.\n` +
    `export const VO_FRAMES = [${frames.join(', ')}] as const;\n` +
    `export const VO_SECONDS = [${seconds.join(', ')}] as const;\n`,
);

console.log(`\nVOICE DONE — ${LINES.length} clips, ${totalVo.toFixed(1)}s total VO`);
console.log(`wrote ${META}`);
