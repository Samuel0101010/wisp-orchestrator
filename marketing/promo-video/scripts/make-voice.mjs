// Generates the voice-over clips with Piper (offline neural TTS), one WAV per
// line. Requires the Piper binary + voice model under .tts/ (see README) — those
// are gitignored; the produced clips in public/audio/vo/ are committed so the
// project renders without re-running TTS.
//
// Also emits src/vo-meta.ts (clip lengths in frames @30fps) so the timeline in
// scenes.config.ts can size every scene to its narration automatically — audio
// and visuals can never drift out of sync.
//
// Usage:  node scripts/make-voice.mjs
